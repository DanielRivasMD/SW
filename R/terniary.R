#' Terniary operator
#'
#' \code{?}             If
#'
#' @return              Invisible
#'
#' @examples T ? T : F
#' @examples F ? T : F
#'
#' @rdname terniary
#' @export

####################################################################################################

`?` <- function(x, y)
  eval(
    sapply(
      strsplit(
        deparse(substitute(y)),
      ":"
    ), 
    function(e) parse(text = e)
  )[[2 - as.logical(x)]])

