#' Check for Absence
#'
#' \code{\%ni\%}        Not in
#'
#' @param x             Object to check
#' @param y             Object to check
#'
#' @return              Invisible
#'
#' @examples 10 %ni% 1:9
#'
#' @rdname check
#' @export

####################################################################################################

'%ni%' <- function(x, y) {
  !('%in%'(x, y))
}

